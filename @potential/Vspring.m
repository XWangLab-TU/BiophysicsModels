function [V] = Vspring(r1,r2,l0,k,varargin)
%--------------------------------------------------------------------------
        
%--------------------------------------------------------------------------
ip = inputParser;
ip.CaseSensitive = true;
ip.addRequired('l0', @(x) isnumeric(x));
ip.addRequired('k', @(x) isnumeric(x));
ip.parse(l0,k,varargin{:});
%----------------------------------------------------------------------------------------
%% computation
D_r_2=(sum((r1-r2).^2,2));
V=-k*D_r_2;
%--------------------------------------------------------------------------    
end

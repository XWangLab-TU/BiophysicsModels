classdef ModActin
%==========================================================================
%==========================================================================     
properties
    var
    pm
    prop
    failInfo
end
%==========================================================================
%==========================================================================     
methods
%--------------------------------------------------------------------------
%--------------------------------------------------------------------------
    function obj = ModActin(varargin)
       ip = inputParser;
       ip.CaseSensitive = false;
       ip.addParameter('unit', [], @isobject);
       ip.parse(varargin{:});
%--------------------------------------------------------------------------            
        obj.prop = {'Particle'};
        obj.failInfo='';
%--------------------------------------------------------------------------
        unit=ip.Results.unit;
            if isempty(unit)
                unit=ComUnit('erg',ComUnit.nm_to_cm(1),300,ComUnit.kBT_to_erg(1,300)); 
                warning('no unit assigned, using 1nm, 1kBT and 300K as natural units');
            end
%--------------------------------------------------------------------------            
        [obj] = SetParameter(obj,'unit',unit);
        [obj] = SetVar(obj);
%--------------------------------------------------------------------------        
    end
%--------------------------------------------------------------------------
%--------------------------------------------------------------------------
     [f] = plot(obj,varargin);
     [obj] = Polymerization(obj, varargin);
     [obj] = Depolymerization(obj, varargin);
     [obj] = Branching(obj,Las17,Mesh,varargin);
     [obj] = Severing(obj, varargin);
     [obj] = Capping(obj, varargin);
%==========================================================================
end
%==========================================================================    
    methods (Static)

    end
%==========================================================================    
end